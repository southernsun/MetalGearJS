    // Instrument IDs
    public const int Drumkit = 1;
    public const int SlapBass = 4;
    public const int Bass = 5;

    /// <summary>
    /// Get instrument data for a specific instrument and note.
    /// </summary>
    public static byte[]? GetInstrument(int instrumentId, int note)
    {
        return instrumentId switch
        {
            Drumkit => GetDrumkit(note),
            SlapBass => GetSlapBass(note),
            Bass => GetBass(note),
            _ => null
        };
    }

    /// <summary>
    /// Drumkit - 12 drum sounds accessed by note value
    /// Note 0  (C):  Hi-hat
    /// Note 1  (C#): Cymbal
    /// Note 2  (D):  Snare drum
    /// Note 3  (D#): Snare drum 2 (shorter)
    /// Note 4-6:     Dummy (silent)
    /// Note 7  (G):  Tom 1 (highest pitch)
    /// Note 8  (G#): Tom 2
    /// Note 9  (A):  Tom 3
    /// Note 10 (A#): Tom 4 (lowest pitch)
    /// Note 11 (B):  Bass drum
    /// </summary>
    private static byte[]? GetDrumkit(int note)
    {
        return note switch
        {
            0 => HiHat,
            1 => Cymbal,
            2 => SnareDrum,
            3 => SnareDrum2,
            4 or 5 or 6 => DummyDrum,
            7 => Tom1,
            8 => Tom2,
            9 => Tom3,
            10 => Tom4,
            11 => BassDrum,
            _ => null
        };
    }

    /// <summary>
    /// Slap bass - percussive bass sound
    /// </summary>
    private static byte[]? GetSlapBass(int note)
    {
        return note switch
        {
            4 => SlapBass1,
            7 => SlapBass2,
            9 => SlapBass3,
            11 => SlapBass4,
            _ => null
        };
    }

    /// <summary>
    /// Bass - standard bass tone
    /// </summary>
    private static byte[]? GetBass(int note)
    {
        return note switch
        {
            0 => Bass1,
            1 => Bass2,
            2 => Bass3,
            4 => Bass4,
            5 => Bass5,
            6 => Bass6,
            7 => Bass7,
            9 => Bass8,
            11 => Bass9,
            _ => null
        };
    }

